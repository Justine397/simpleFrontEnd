function redBG(){
    document.body.style.background = 'red';
    setTimeout(BackToOG, 2000);
}
function greenBG(){
    document.body.style.background = 'green';
    setTimeout(BackToOG, 2000);
}
function blueBG(){
    document.body.style.background = "blue";
    setTimeout(BackToOG, 2000);
}
function BackToOG(){
    document.body.style.background = 'yellow';
}